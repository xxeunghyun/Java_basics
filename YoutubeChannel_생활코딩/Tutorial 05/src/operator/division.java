package operator;

public class division {

	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		
		float c = 10.0F;
		float d = 3.0F;
		
		System.out.println(a/b); //정수의 나눗셈
		System.out.println(c/d); //실수의 나눗셈
		System.out.println(a/d); //정수와 실수의 나눗셈 >더 detail한 형식으로 표현 
		System.out.println(c/b); //실수와 정수의 나눗셈 

	}

}
